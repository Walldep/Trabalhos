package repetição_FOR;

public class repetição_FOR {

	public static void main(String[] args) {
	/* repetição FOR */
		
		for (int numero = 0; numero <= 10; numero ++) {
			System.out.println("numero atual é: " + numero);
		}
	}

}
/* for (int numero = 10; numero >= 0; numero --) repetição sequencial inversa */
/* for (int numero = 0; numero <= 10; numero ++) repetição sequencial normal */