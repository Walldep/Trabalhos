package java_concatenação;

public class concatenação {
	public static void main(String[] args) {
		
		/* concatenação usado para unir ou juntar dados*/
		String nome = "sabino";
		String cpf = "455.888.999.11";
		String telefone = "77 7788-5588";
		String endereço = "sede - PF";
		
		String saida = "meu nome e: " + nome + " e o cpf: " + cpf + " meu contato: " + telefone + " local: " + endereço;
		
		System.out.println(saida);
		
	}
}
