
public class SwitchCase {

	public static void main(String[] args) {
		/* Switch e case: operações exatas */
		int dia = 1;
		

		switch (dia) {
		case 1:
			System.out.println("domingo");
			break;
		case 2:
			System.out.println("segunda");
			break;
		
		default:
			System.out.println("outro dia da semana");
			break;
		}
	}

}
