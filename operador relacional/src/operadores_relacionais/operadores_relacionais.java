package operadores_relacionais;

public class operadores_relacionais {

	public static void main(String[] args) {
	/* operador 
	 *  	== (igual) 
	 * operador 
	 *  	!= (diferente) 
	 * operador
	 *  	< (menor) 
	 * operador 
	 *  	> (maior) 
	 * operador
	 *  	<= (menor igual)
	 * operador
	 *  	>= (maior igual) */
		
		int nota1 = 90;
		int nota2 = 90;
		
		if (nota1 == nota2) {
			System.out.println("notas iguais");
		}else {
			System.out.println("notas diferentes");
		}
	}

}
