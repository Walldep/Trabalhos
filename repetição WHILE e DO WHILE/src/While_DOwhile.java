
public class While_DOwhile {

	public static void main(String[] args) {

		/* estrutra de repetição While(verifica e executa) e DO While */

		int numero = 0;

		while (numero <= 11) {
			System.out.println("numero atual é: " + numero);
			numero++;
		}

		int numero2 = 0;

		do {
			System.out.println("numerto atual é: " + numero2);
			numero2 ++;
		} while (numero2 <= 9);
	}
}
